				<p>Scris chineză a fost cunoscut în Coreea de peste 2000 de ani. Acesta a fost utilizat pe scară largă în timpul ocupației chinez din Coreea de Nord de la 108 î.Hr. la 313 AD. Prin secolul 5, coreenii au început să scrie în limba chineză clasică - cel mai vechi cunoscut al acestei datează din 414 AD. Ei au inventat mai târziu , trei sisteme diferite pentru scrierea coreeană cu caractere chinezești: Hyangchal (향찰 /鄕札), Gukyeol (구결 /口訣) și Idu (이두 /吏讀). Aceste sisteme au fost similare cu cele dezvoltate în Japonia și au fost , probabil , folosite ca modele de către japonezi.

				Idu Sistemul utilizat o combinație de caractere chinezești , împreună cu simboluri speciale pentru a indica finaluri verb coreene și a altor markeri gramaticale, și a fost utilizat în documentele oficiale și private pentru multe secole. Hyangchal Sistemul folosit de caractere chinezești pentru a reprezenta toate sunetele coreene și a fost folosit în principal pentru a scrie poezie.
				Coreenii împrumutat un număr foarte mare de cuvinte din China, a dat lecturi și / sau înțelesuri coreene la unele dintre caracterele chinezești și, de asemenea, a inventat aproximativ 150 de caractere noi, dintre care cele mai multe sunt rare sau utilizate în principal pentru numele personale sau de loc.
				Alfabetul coreean a fost inventat în 1444 și a promulgat - o în 1446 în timpul domniei regelui Sejong (r.1418-1450), al patrulea rege al dinastiei Joseon. Alfabetul a fost inițial numit Hunmin jeongeum , sau „corecte sunete pentru instruirea poporului“, dar a fost , de asemenea , cunoscut sub numele Eonmeun (script - ul vulgar) și Gukmeun (scris național). Numele modern pentru alfabetul, Hangeul , a fost inventat de un lingvist coreean numit Ju Si-Gyeong (1876-1914). În Coreea de Nord alfabetul este cunoscut sub numele de 조선 글 (josoen guel).
				Formele consoane se bazează pe forma gurii făcut atunci când se face sunetul corespunzător, iar direcția tradițională a scris (pe verticală, de la dreapta la stânga), cel mai probabil a venit din China, așa cum a făcut practica de a scrie silabe în blocuri.

